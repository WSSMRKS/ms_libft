#include "../libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	const char	*src_;
	char		*dest_;

	src_ = src;
	dest_ = dest;
	if (!dest && !src)
		return (0);
	while (n > 0)
	{
		n--;
		dest_[n] = src_[n];
	}
	return (dest);
}
